interface Props {
  role: string;
}

export function GestionSidebar({ role }: Props) {
  const pathname = usePathname();
  const isElevated = ELEVATED_ROLES.includes(role);
  const visibleItems = NAV_ITEMS.filter(item => !item.elevated || isElevated);

  return (
    <Sidebar>
      <Sidebar.Header label="Panel" />
      <Sidebar.Nav>
        {visibleItems.map((item) => {
          const isActive = item.href === '/gestion'
            ? pathname === '/gestion'
            : pathname === item.href || pathname.startsWith(`${item.href}/`);

          if (item.disabled) {
            return (
              <Sidebar.Item
                key={item.href}
                disabled
                icon={item.icon}
              >
                {item.label}
              </Sidebar.Item>
            );
          }

          return (
            <Sidebar.Item
              key={item.href}
              as={Link}
              href={item.href}
              active={isActive}
              icon={item.icon}
            >
              {item.label}
            </Sidebar.Item>
          );
        })}

        {isElevated && (
          <Sidebar.Item
            as={Link}
            href={CONFIG_ITEM.href}
            active={pathname === CONFIG_ITEM.href || pathname.startsWith(`${CONFIG_ITEM.href}/`)}
            icon={CONFIG_ITEM.icon}
          >
            {CONFIG_ITEM.label}
          </Sidebar.Item>
        )}
      </Sidebar.Nav>
    </Sidebar>
  );
}
